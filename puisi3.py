import os
import time
import sys

os.system('clear')
def mulai(memek):
    for kontol in memek:
        sys.stdout.write(kontol)
        sys.stdout.flush()
        time.sleep(80. / 1000)
mulai ('''

>>>>>>>>>>>>>> LOADING >>>>>>>>>>>
''')

